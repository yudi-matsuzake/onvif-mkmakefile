# copy the directory 
if [ ! $(diff "${MKMKFILE_PATH}/fixprox" "${PWD}/fixprox" &> /dev/null ) ]
then
	cp -r ${MKMKFILE_PATH}/fixprox ./.fixprox
fi

URL=
BASENAME=
PROXY_CLIENT=
HPP_HEADER=

# build arrays
i=0
while read url
do
	base="$(basename "$url")"
	URL[$i]="$url"
	BASENAME[$i]="${base%%.*}"
	PROXY_CLIENT[$i]="${BASENAME[$i],,}_client_proxy"
	HPP_HEADER[$i]="${BASENAME[$i]}/${BASENAME[$i]}.hpp"

	i=$(( i+1 ))

done < ${WSDL_FILE}

# makefile's header
echo "all: ${HPP_HEADER[@]} env"
echo
echo "GSOAP_PATH=/usr/share/gsoap/"

# url variables
echo
echo "# urls"
for (( i=0; i<"${#URL[@]}"; i++ ))
do
	mkurl "${BASENAME[$i]}" "${URL[$i]}"
done


# dir variables
echo
echo "# paths"
for (( i=0; i<"${#URL[@]}"; i++ ))
do
	mkpath "${BASENAME[$i]}"
done

# header variables
echo
echo "# headers"
for (( i=0; i<"${#URL[@]}"; i++ ))
do
	mkheader "${BASENAME[$i]}"
done

# namespace variables
echo
echo "# namespaces"
