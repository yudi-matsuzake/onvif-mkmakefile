do
	proxy_rule "${BASENAME[$i]}"
	echo
done

# header cpp rules
echo
echo "# CPP HEADERS RULES--------------------------------------------------"
for (( i=0; i<"${#URL[@]}"; i++ ))
do
	cpp_header_rule "${BASENAME[$i]}"
	echo
done
