do
	mknamespace "${BASENAME[$i]}"
done

# header variables
echo
echo "# cpp headers"
for (( i=0; i<"${#URL[@]}"; i++ ))
do
	makecppheader "${BASENAME[$i]}"
done

# env rule
env_rule

# dir rules
echo
echo "# PATH RULES---------------------------------------------------------"
